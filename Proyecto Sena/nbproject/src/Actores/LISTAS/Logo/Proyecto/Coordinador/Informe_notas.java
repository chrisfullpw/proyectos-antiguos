    private void mostrar(){
        
        DefaultTableModel modelo = new DefaultTableModel();
        ResultSet rs = Conexion.getTable("select e.nombre as materia, (nota1+nota2+nota3+nota4+nota5+nota6+nota7+nota8)/8 as promedio from ((materia as e\n" +
" inner join primertrimestre on primertrimestre.id_materia1 = e.id_materia)\n" +
" inner join estudiante on primertrimestre.id_est = estudiante.id_estudiante)\n" +
" where nom_est ='"+txtNombre.getText()+"' and apell_est = '"+txtApellido.getText()+"' and id_cursos1 ='"+txtcursos.getText()+"';");
                
        modelo.setColumnIdentifiers(new Object[]{"materia","promedio"});
        
        //(3, curso.getText());
        try {
            while(rs.next()){
            modelo.addRow(new Object[]{rs.getString("materia"),rs.getString("promedio")});
            }
            
            //Asigna el modelo a la tabla
            jTableInfor.setModel(modelo);
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    };
