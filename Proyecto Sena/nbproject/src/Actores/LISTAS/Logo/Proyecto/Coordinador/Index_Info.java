            private void mostrar(){
        
        DefaultTableModel modelo = new DefaultTableModel();
        ResultSet rs = Conexion.getTable("select eventos from coordinador;");
        modelo.setColumnIdentifiers(new Object[]{"eventos"});
        
        //(3, curso.getText());
        try {
            while(rs.next()){
            modelo.addRow(new Object[]{rs.getString("eventos")});
            }
            
            //Asigna el modelo a la tabla
            Even.setModel(modelo);
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    };
