    public DefaultTableModel mostrar(){
    Conexion con= new Conexion();
    DefaultTableModel modelo = new DefaultTableModel();
    String sql = "select id_aulas, disponibilidad from aulas";
    
        
    
    ResultSet rs = Conexion.getTable("select id_aulas, disponibilidad from aulas");
    modelo.setColumnIdentifiers(new Object[]{"Aulas","Disponibilidad"});
    
        try {
            while (rs.next()) {
                modelo.addRow(new Object[]{rs.getString("id_aulas"),rs.getString("disponibilidad")});
            }
           //asignar el modelo a la tabla
           JTableAulas.setModel(modelo);
           
        } catch (Exception e) {
            System.out.println(e.toString());
        }
        return modelo;
    }
