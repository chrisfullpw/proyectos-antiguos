    public void modificar(){
        try {
            conexion con = new conexion();
            cn = con.conexion();
            
            String sql = "update aulas set disponibilidad = ? where id_aulas = '"+txtAula.getText()+"';";
            
            PreparedStatement pst = cn.prepareCall(sql);
            pst.setString(1, txtDisponibilidad.getText());
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Aula Actualizada");
            pst.close();
            cn.close();
           
            
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        //mostrar();
    }
    
