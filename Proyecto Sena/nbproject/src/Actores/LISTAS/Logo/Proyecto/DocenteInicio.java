     public void ValidarAcceso(){
    int resultado=0;
    
        try {
            String usuario= Usuario.getText();
            String pas= String.valueOf(pass.getPassword());
            
            String sql="select * from docente where ID_docente='"+usuario+"'and contraseña='"+pas+"';";
                    
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(sql);
            
            if (rs.next()) {
                resultado=1;
                if (resultado==1) {
                    JOptionPane.showMessageDialog(null, "Bienvenido");
                    DocenteIndex doc = new DocenteIndex();
                    doc.setVisible(true);
                    this.dispose();
                    
                }else{
                    System.out.println("Que crazy");
                    }
                
                        
            } else{
            if (resultado==0) {
                        JOptionPane.showMessageDialog(null, "Contraseña o Usuario incorrecto, Intente de nuevo");
                        this.setVisible(true);
            }}
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error, intente de nuevo"+e.getMessage());
        }
    
    
    }
    
    
