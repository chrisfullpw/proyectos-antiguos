    private void btn_enviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_enviarActionPerformed
try{
            PreparedStatement ppt = cn.prepareStatement("INSERT INTO coordinadores(nombres, apellidos, cedula, fechadeexpedicion, usuario, contraseña, telefono) VALUES(?,?,?,?,?,?,?)");
            ppt.setString(5, txt_usuarioC.getText());
            ppt.setString(6, psw_C.getText().toString());
            ppt.setString(1, txt_nombreC.getText());
            ppt.setString(2, txt_apellidoC.getText());
            ppt.setString(7, txt_telefonoC.getText());
            ppt.setString(3, txt_cedulaC.getText());
            ppt.setString(4, txt_fechaC.getText());

            
            ppt.executeUpdate();
           
        }catch(Exception e){
        }
       
     if((txt_nombreC.getText().equals("")) || (txt_apellidoC.getText().equals("")) || (txt_cedulaC.getText().equals(""))
             || (txt_fechaC.getText().equals("")) || (txt_usuarioC.getText().equals("")) || (psw_C.getText().toString().equals(""))
             || (txt_telefonoC.getText().equals("")))
             { 
                 javax.swing.JOptionPane.showMessageDialog(this, "debe llenar todos los campos \n","AVISO!",javax.swing.JOptionPane.INFORMATION_MESSAGE);
          txt_nombreC.requestFocus();
             }
    
             else{
         javax.swing.JOptionPane.showMessageDialog(this,"Registro exitoso! \n","AVISO!",javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
        this.txt_nombreC.setText("");
        this.txt_apellidoC.setText("");
        this.txt_telefonoC.setText("");
        this.txt_cedulaC.setText("");
        this.psw_C.setText(""); 
        this.txt_fechaC.setText("");
        this.txt_usuarioC.setText("");

    }//GEN-LAST:event_btn_enviarActionPerformed

